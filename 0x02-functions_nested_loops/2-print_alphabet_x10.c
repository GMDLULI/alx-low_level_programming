#include "main.h"
/**
 * main - Entry point of program
 *
 * Description: print lowercase alphabets 10times
 *
 * return: Always 0
 */
void print_alphabet_x10(void)
{
	int i = 0;
	int j;

	for (i = 0; i < 11; i++)
	{
		for (j = 'a'; j <= 'z'; j++)
		{
			_putchar('j');
			_putchar('\n');
		}
	}
}


